  function transform(){
    const inputChat=document.getElementById('InputChat');
    if(inputChat===null)return;
    let text=inputChat.value;
    if(text.length>0&&text[0]==' ')return true;
    // Bold
    text=ChatBox.substituteRegex(
      text,/^(.*)\*(.+)\*(.*)$/,ChatBox.sansBold);
    // Circle
    text=ChatBox.substituteRegex(
      text,/^(.*)\(\((.+)\)\)(.*)$/,ChatBox.circleWhite);
    // Courier
    text=ChatBox.substituteRegex(
      text,/^(.*)=(.+)=(.*)$/,ChatBox.courier);
    // Cursive
    if(text.match(/^(.*)~~(.*)~(.*)$/)===null
       &&text.match(/^(.*)~(.*)~~(.*)$/)===null)
      text=ChatBox.substituteRegex(
        text,/^(.*)~(.+)~(.*)$/,ChatBox.cursive);
    text=ChatBox.substituteRegex(
      text,/^(.*)~~(.+)~~(.*)$/,ChatBox.cursiveBold);
    // Fraktur
    if(text.match(/^(.*)##(.*)#(.*)$/)===null
       &&text.match(/^(.*)#(.*)##(.*)$/)===null)
      text=ChatBox.substituteRegex(
        text,/^(.*)#(.+)#(.*)$/,ChatBox.fraktur);
    text=ChatBox.substituteRegex(
      text,/^(.*)##(.+)##(.*)$/,ChatBox.frakturBold);
    // Italic
    if(text.match(/^(.*)\/\/(.*)\/(.*)$/)===null
       &&text.match(/^(.*)\/(.*)\/\/(.*)$/)===null)
      text=ChatBox.substituteRegex(
        text,/^(.*)\/(.+)\/(.*)$/,ChatBox.sansItalic);
    text=ChatBox.substituteRegex(
      text,/^(.*)\/\/(.+)\/\/(.*)$/,ChatBox.sansBoldItalic);
    // Outline
    text=ChatBox.substituteRegex(
      text,/^(.*)\|(.+)\|(.*)$/,ChatBox.outline);
    // Square
    text=ChatBox.substituteRegex(
      text,/^(.*)\[\[(.+)\]\](.*)$/,ChatBox.squareWhite);
    // Underline
    text=ChatBox.substituteRegex(
      text,/^(.*)_(.+)_(.*)$/,ChatBox.underline);
    inputChat.value=text;
    return true;}

  function substituteRegex(text,regex,newAlphabet){
    const match=text.match(regex);
    if(match===null)return text;
    if(match.length!=4)return text;
    match[2]=ChatBox.substituteAll(match[2],newAlphabet);
    return match[1]+match[2]+match[3];}

  function substituteAll(oldText,newAlphabet){
    let newText='';
    for(let i=0;i<oldText.length;++i){
      let char=oldText[i];
      // @ Pass this in
      let j=ChatBox.oldAlphabet.indexOf(char);
      if(j>=0)char=newAlphabet[j];
      newText=newText+char;}
    return newText;}

